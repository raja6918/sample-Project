package com.adopt.altitude.automation.frontend.data.operatingFlights;

public class OfExtraTime {

	private String name;
	private String briefMinutes;
	private String debriefMinutes;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBriefMinutes() {
		return briefMinutes;
	}

	public void setBriefMinutes(String briefMinutes) {
		this.briefMinutes = briefMinutes;
	}
	
	public String getDebriefMinutes() {
		return debriefMinutes;
	}
	
	public void setDebriefMinutes(String debriefMinues) {
		this.debriefMinutes = debriefMinues;
	}

}
