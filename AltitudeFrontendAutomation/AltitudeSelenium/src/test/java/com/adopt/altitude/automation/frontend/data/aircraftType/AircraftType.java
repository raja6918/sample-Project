package com.adopt.altitude.automation.frontend.data.aircraftType;

public class AircraftType {

  private String iATAType;

  private String model;

  private String name;

  private String restFacility;

  public String getIataType() {
    return iATAType;
  }

  public void setIataType(String iATAType) {
    this.iATAType = iATAType;
  }

  public String getModel() {
    return model;
  }

  public void setModel(String model) {
    this.model = model;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getRestFacility() {
    return restFacility;
  }

  public void setRestFacility(String restFacility) {
    this.restFacility = restFacility;
  }
}
